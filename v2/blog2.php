                <div class="fw-bold" style="font-size: 1.05rem; margin-bottom: 0.3rem;">Implications for the Motor Finance Industry</div>
                <div class="paragraph" style="margin-bottom: 1.1rem;">This ruling has set a precedent for greater transparency and accountability in the motor finance sector. Unless overturned by the Supreme Court, car finance providers and brokers must:.</div>
                <ul class="paragraph" style="margin-bottom: 1.1rem;">
                    <li>Fully disclose commission arrangements to customers.</li>
                    <li>Obtain informed consent before finalising finance agreements.</li>
                    <li>Review and potentially revise their policies to comply with the Court of Appeal's decision.</li>
                </ul>
            </div>
        </div>   
         

        <div class="mt-5">
            <div class="fw-bold" style="font-size: 1.05rem; margin-bottom: 0.3rem;">The Road Ahead</div>
            <div class="paragraph" style="margin-bottom: 0.7rem;">The lenders involved in this case have appealed the decision to the Supreme Court. While the appeal is pending, the Court of Appeal's judgment remains binding. This means motor finance providers must comply with the ruling unless it is overturned.</div>
            <div class="paragraph" style="margin-bottom: 1.1rem;">The Financial Conduct Authority (FCA) has also extended complaint response deadlines to accommodate the potential surge in related complaints. Providers now have until after 4 December 2025 to respond to complaints about commission arrangements.</div>
        </div>


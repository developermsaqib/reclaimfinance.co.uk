            <div class="fw-bold" style="font-size: 1.05rem; margin-bottom: 0.3rem;">Key Takeaways</div>
            <ul class="paragraph" style="margin-bottom: 1.1rem;">
                <li>The Hopcroft v Close Brothers ruling reinforces the importance of transparency in car finance agreements.</li>
                <li>Consumers should be informed of any commission payments and their impact on loan terms.</li>
                <li>If you've been affected by undisclosed commissions, take action to protect your rights.</li>
            </ul>

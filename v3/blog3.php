                    <p class="paragraph">In September 2024, the FCA extended its review into historical discretionary commission arrangements in the motor finance sector. If widespread misconduct is identified, the FCA will consider compensating affected consumers. Temporary complaint-handling rules have been introduced to manage related complaints.</p>

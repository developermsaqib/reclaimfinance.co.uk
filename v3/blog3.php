            <p class="paragraph fw-bold" style="font-size: 1.05rem;">Latest Updates</p>
     
            <p class="paragraph">On 19 December 2024, the FCA announced temporary rules on motor finance commission to extend how long:</p>
                <ul class="paragraph">
                    <li>Firms have to respond to motor finance complaints where a 'non-discretionary commission arrangement' was involved.</li>
                    <li>Consumers have to refer complaints to the FOS.</li>
                </ul>
                <p class="paragraph">This means that the rules for all complaints about motor finance agreements where there were commission arrangements in place are now in line with those announced in January 2024, which covered agreements linked to discretionary commission arrangements.</p>
                <p class="paragraph">Ongoing legal action involving commission arrangements in motor finance could also affect the FOS's work on complaints involving similar issues. The Supreme Court's findings, which will consider an appeal of the recent judgment in Johnson, Wrench, and Hopcraft set for April 2025, might influence future decisions.</p>
                <p class="paragraph">Additionally, on 17 December 2024, the High Court found in FOS's favour in a review of its decision to uphold a complaint related to a discretionary commission arrangement (DCA) in a motor finance agreement. This judgment is being carefully reviewed to assess its implications for other similar cases.</p>
                <p class="paragraph fw-bold" style="font-size: 1.05rem;">Types of Complaints</p>
                <p class="paragraph">The FOS receives complaints about car finance commission paid by lenders to car dealers acting as credit brokers. These complaints arise from a variety of car finance agreements and commission models. Common issues reported include:</p>
                <ul class="paragraph">
                    <li>The car finance agreement was arranged unfairly.</li>
                    <li>Commissions were not properly disclosed, affecting the interest paid or loan type received.</li>
                    <li>Credit brokers or intermediaries did not disclose their commission from lenders.</li>
                    <li>Consumers were not told how commission was paid or perceived as unfair.</li>
                </ul>


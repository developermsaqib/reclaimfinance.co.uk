                    <p class="paragraph">The FOS continues to manage complaints amidst ongoing legal proceedings. The Supreme Court's decision on cases like Johnson v. FirstRand Bank Limited and others will provide further guidance. The FOS will:</p>
                    <ul class="paragraph">
                        <li>Gather necessary information to decide complaints when appropriate.</li>
                        <li>Identify complaints affected by court rulings.</li>

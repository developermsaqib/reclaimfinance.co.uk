            <p class="paragraph mt-4" >  <strong>Complaints About Discretionary Commission Arrangements (DCAs)</strong> <br /> 
                Before 2021, some lenders allowed brokers to adjust the interest rates offered on car finance. Brokers earned higher commissions by securing higher interest rates, creating a potential conflict of interest. These arrangements, known as discretionary commission arrangements (DCAs), were banned in 2021.</p>
            </div>

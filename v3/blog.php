            <div class="fw-semibold" style="margin-bottom: 0.2rem;">To manage the anticipated volume of complaints following the Court of Appeal ruling, the FCA has extended deadlines:</div>
            <p class="paragraph" style="margin-bottom: 1.2rem;">Providers have until after <b>4 December 2025</b> to respond to complaints about all types of car finance commission. Consumers have until at least <b>29 July 2026</b> to refer complaints to the FOS.</p>

            <div class="fw-bold" style="font-size: 1.1rem; margin-bottom: 0.2rem;">FCA Review and Future Updates</div>
            <p class="paragraph">The FCA is currently reviewing DCAs and plans to announce next steps in May 2025. This review will also address complaints about non-DCA commission arrangements. The outcome may depend on the progress and decisions of the Supreme Court's appeal listed for April 2025.</p>

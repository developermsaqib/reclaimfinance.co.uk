            <div class="blog-title">FCA Updates on Car<br>Finance Commission<br>Complaints</div>
            <p class="paragraph">If you've used car finance to purchase a car, motorbike, or van, it's likely that the lender paid the broker (usually your car dealer) a commission for arranging the loan. Recent updates from the Financial Conduct Authority (FCA) and the Court of Appeal have significant implications for such arrangements.</p>
            <p class="paragraph"><strong>Court of Appeal Ruling</strong></p>
            <p class="paragraph">On 25 October 2024, the Court of Appeal ruled on three cases involving car finance. The court determined that it was unlawful for dealers to receive a commission from the lender without first informing the customer about the commission and obtaining their informed consent.</p>
            <p class="paragraph"><strong>Informed consent requires disclosure of:</strong></p>
                <p class="paragraph">The amount of the commission.</p>
                <p class="paragraph">How the commission is calculated.</p>
            <p class="paragraph">The lenders involved are appealing this decision to the Supreme Court. Until overturned, this ruling stands as law, and car finance providers must comply.</p>
